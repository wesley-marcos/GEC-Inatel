public class Arma {

    private String arma;
    private boolean magica = true;

    //Getters e Setters
    public String getArma() {
        return arma;
    }

    public void setArma(String arma) {
        this.arma = arma;
    }

    public boolean isMagica() {
        return magica;
    }

    public void setMagica(boolean magica) {
        this.magica = magica;
    }
}
